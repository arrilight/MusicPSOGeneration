public class Point3D {
    private int x, y, z;

    Point3D (int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }


    int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

}
